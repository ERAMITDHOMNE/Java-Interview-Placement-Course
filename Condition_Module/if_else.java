package Condition_Module;

public class if_else // if_else class name
{
    public static void main(String arg[])
    {
        int marks = 2;// declare variable
        if(marks > 35)            // if condition 
        {
            System.out.println("Pass");    // execute the statement
        }
        else
        {
            System.out.println("Fail");     // execute the statement
        }
    
    }
}